let mas1 = [1,2,3];//Первый масив
let mas2 = [1,2,4];//Второй масив
function comparison(a,b){// функция сравнения
  let res; // переменая которая принимает логическое значение
  if(a.length == b.length){ // если длины у масивов разные то false если одинаковые идем дальше
    for(let i = 0; i < a.length; i++){//масив для сравнения каждого элемента
      if(a[i] === b[i]){//если равны элементы масивов, переменной res присваивает значение true иначе false и завершает операцию break
        res = true;
      }else{
        res = false;
        break;
      }
    }
  }else{
    return false;
  }
  return res;
}
