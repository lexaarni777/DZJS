let massRange = []; //создаем пустой массив
function range(a,b,c = 2){ //создаем функцию с 3 аргументами, последний аргумент по умолчанию равен 2
  let ac = a;//создаем переменную значение которой будет вставлятся в масив
  for(let i = 0; i < (b-a)/c; i++){//создем цикл. ((b-a)/c) - это количество элементов которое в итоге должно получится в массиве
    massRange[i] = ac; //создаем значение в масиве по индексу
    ac += c;
  }
  return massRange; // возвращаем массив
}
